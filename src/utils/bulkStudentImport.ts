// Department mapping based on course codes
const getDepartmentFromCourse = (course: string): string => {
  const courseUpper = course.toUpperCase();
  
  // CECE (College of Engineering and Civil Engineering)
  if (['BSCE', 'BSEE', 'BSECE', 'BSECE', 'BSME', 'BSCHE', 'BSIE', 'BSCPE'].includes(courseUpper)) {
    return 'CECE';
  }
  
  // CBA (College of Business Administration)
  if (['BSBA', 'BSA', 'BSMA', 'BSCA', 'BSE'].includes(courseUpper)) {
    return 'CBA';
  }
  
  // CTELAN (College of Technology, Education, Liberal Arts, and Nursing)
  // This includes IT, Education, Liberal Arts, Nursing, etc.
  return 'CTELAN';
};

